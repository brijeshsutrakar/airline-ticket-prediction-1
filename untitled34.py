import streamlit as st
import pandas as pd
import numpy as np
import pickle
from datetime import datetime, time, timedelta

# Load the trained model
try:
    with open('rf_random.pkl', 'rb') as file:
        loaded_model = pickle.load(file)
    st.success("Model loaded successfully!")
except FileNotFoundError:
    st.error("Error: rf_random.pkl not found. Please ensure it's in the same directory as app.py")
    st.stop()

st.set_page_config(page_title="Flight Price Predictor", layout="centered")
st.title("✈️ Flight Price Prediction")
st.write("Enter the flight details below to get an estimated price.")

# --- Encoding Dictionaries (Hardcoded for deployment) ---
# These should be exactly as used during training
dict_airlines = {'Trujet': 0, 'SpiceJet': 1, 'Air Asia': 2, 'IndiGo': 3, 'GoAir': 4, 'Vistara': 5, 'Vistara Premium economy': 6, 'Air India': 7, 'Multiple carriers': 8, 'Multiple carriers Premium economy': 9, 'Jet Airways': 10, 'Jet Airways Business': 11}
dict_dest = {'Kolkata': 0, 'Hyderabad': 1, 'Delhi': 2, 'Banglore': 3, 'Cochin': 4}
stop = {'non-stop': 0, '2 stops': 2, '1 stop': 1, '3 stops': 3, '4 stops': 4}

# --- Streamlit Input Widgets ---
st.header("Flight Details")

col1, col2 = st.columns(2)
with col1:
    airline_input = st.selectbox("Airline:", list(dict_airlines.keys()))
    source_input = st.selectbox("Source City:", ['Banglore', 'Kolkata', 'Delhi', 'Chennai', 'Mumbai'])
    dep_time_input = st.time_input("Departure Time:", value=time(10, 0))
    total_stops_input = st.selectbox("Total Stops:", list(stop.keys()))

with col2:
    destination_input = st.selectbox("Destination City:", ['New Delhi', 'Banglore', 'Cochin', 'Kolkata', 'Delhi', 'Hyderabad'])
    date_of_journey_input = st.date_input("Date of Journey:", value=datetime.now().date())
    arrival_time_input = st.time_input("Arrival Time:", value=time(12, 0))


if st.button("Predict Price 🚀"):
    # --- Feature Engineering ---
    # 1. Date_of_Journey features
    journey_day = date_of_journey_input.day
    journey_month = date_of_journey_input.month

    # 2. Dep_Time features
    dep_time_hour = dep_time_input.hour
    dep_time_minute = dep_time_input.minute

    # 3. Arrival_Time features
    arrival_time_hour = arrival_time_input.hour
    arrival_time_minute = arrival_time_input.minute

    # 4. Handle Destination (replace 'New Delhi' with 'Delhi' before mapping)
    processed_destination = destination_input
    if processed_destination == 'New Delhi':
        processed_destination = 'Delhi'

    # Check if processed_destination exists in dict_dest
    if processed_destination not in dict_dest:
        st.error(f"Invalid destination: {processed_destination}")
        st.stop()

    # 5. Calculate Duration to derive 'Duration_hours', 'Duration_mins', 'Duration_hour', 'Duration_minute'
    dep_datetime = datetime.combine(date_of_journey_input, dep_time_input)
    arrival_datetime = datetime.combine(date_of_journey_input, arrival_time_input)

    # Handle cases where arrival is on the next day
    if arrival_datetime < dep_datetime:
        arrival_datetime += timedelta(days=1)

    duration_delta = arrival_datetime - dep_datetime
    total_duration_minutes = int(duration_delta.total_seconds() / 60)

    # Ensure at least 0h 0m format for duration string if total_duration_minutes is 0
    if total_duration_minutes < 0:
        st.error("Arrival time cannot be before departure time on the same or next day.")
        st.stop()

    # Reconstruct 'Duration' string as in the notebook to extract all 4 duration features consistently
    duration_h_val = total_duration_minutes // 60
    duration_m_val = total_duration_minutes % 60

    duration_str = f"{duration_h_val}h {duration_m_val}m"
    if duration_h_val == 0 and duration_m_val == 0:
        duration_str = "0h 0m"
    elif duration_h_val == 0:
        duration_str = f"0h {duration_m_val}m"
    elif duration_m_val == 0:
        duration_str = f"{duration_h_val}h 0m"

    # Extracting the 4 duration features
    duration_hours = int(duration_str.split(' ')[0][:-1])
    duration_mins = int(duration_str.split(' ')[1][:-1])

    # Using pd.to_timedelta for 'Duration_hour' and 'Duration_minute' as in the notebook
    td = pd.to_timedelta(duration_str)
    duration_hour = td.components.hours
    duration_minute = td.components.minutes

    # Create a DataFrame for prediction
    # Ensure column order matches X_train used for model training
    input_features = [
        dict_airlines[airline_input],
        dict_dest[processed_destination],
        stop[total_stops_input],
        journey_day,
        journey_month,
        dep_time_hour,
        dep_time_minute,
        arrival_time_hour,
        arrival_time_minute,
        duration_hours,
        duration_mins,
        duration_hour,
        duration_minute,
        1 if source_input == 'Banglore' else 0,
        1 if source_input == 'Kolkata' else 0,
        1 if source_input == 'Delhi' else 0,
        1 if source_input == 'Chennai' else 0,
        1 if source_input == 'Mumbai' else 0
    ]

    # The feature names from X.columns are:
    # Index(['Airline', 'Destination', 'Total_Stops', 'Journey_day',
    #        'Journey_month', 'Dep_Time_hour', 'Dep_Time_minute',
    #        'Arrival_Time_hour', 'Arrival_Time_minute', 'Duration_hours',
    #        'Duration_mins', 'Duration_hour', 'Duration_minute', 'Source_Banglore',
    #        'Source_Kolkata', 'Source_Delhi', 'Source_Chennai', 'Source_Mumbai'],
    #       dtype='object')

    columns_order = ['Airline', 'Destination', 'Total_Stops', 'Journey_day', 'Journey_month',
                     'Dep_Time_hour', 'Dep_Time_minute', 'Arrival_Time_hour', 'Arrival_Time_minute',
                     'Duration_hours', 'Duration_mins', 'Duration_hour', 'Duration_minute',
                     'Source_Banglore', 'Source_Kolkata', 'Source_Delhi', 'Source_Chennai', 'Source_Mumbai']

    input_df = pd.DataFrame([input_features], columns=columns_order)

    # Make prediction
    prediction = loaded_model.predict(input_df)[0]

    st.subheader(f"Predicted Flight Price: ₹ {prediction:.2f}")

st.markdown("---")
st.info("Note: This prediction is based on the trained model and may vary from actual prices.")